// Helper function to get country by code
export function getCountryByCode(code: string): CountryCode | undefined {
  return countryCodes.find(c => c.code === code);
}

// Helper function to validate phone number for a specific country
export function validatePhoneForCountry(phone: string, countryCode: string): boolean {
  const country = getCountryByCode(countryCode);
  if (!country) return false;

  // Remove all non-digit characters
  const digits = phone.replace(/\D/g, '');

  // Check if length is within range
  return digits.length >= country.minDigits && digits.length <= country.maxDigits;
}
